package com.epam.esm.exception;

/**
 * Class container with custom error codes.
 * <p>
 * Variable names show the prototype error codes.
 * For example {@code CODE_404} equals to status code <i>"404 Not Found"</i>
 */
public class CustomErrorCode {

    public static final int CODE_404 = 40401;

    public static final int CODE_500 = 50001;

    public static final int CODE_400 = 40001;

}